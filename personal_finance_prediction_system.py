"""## Data Preprocessing

The dataset is cleaned by handling missing values, converting data types, and preparing it for analysis. This step ensures consistency and accuracy in further processing.
"""

#checking for null values
df.isna().sum()

#dropping null values
df = df.dropna()

#clear view of dataframe
df.columns = df.columns.str.strip()

#conversion to datetime format
df['Date'] = pd.to_datetime(df['Date'], errors='coerce')

"""## Feature Engineering

New features such as Month, Day, and DayOfWeek are extracted from the date column. These features help the model capture patterns related to time and financial behavior.
"""

#extracting month and date
df['Month'] = df['Date'].dt.month
df['Day'] = df['Date'].dt.day

#conversion to numeric
df['Amount'] = pd.to_numeric(df['Amount'], errors='coerce')

df['Amount'] = np.log1p(df['Amount'])

#mapping income and expenses
df['Type'] = df['Type'].map({'Income': 1, 'Expense': 0})

#information about datatypes
df.head()

#conversion to integer
df['Type'] = df['Type'].astype(int)

