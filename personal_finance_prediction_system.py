# ── prediction history stored in-memory ──────────────────────────────────────
_history: list[dict] = []

def predict_finance(amount, date_str):
    log_amount = np.log1p(amount)
    try:
        date_obj = datetime.datetime.strptime(date_str, '%d-%m-%Y')
        month = date_obj.month
        day   = date_obj.day
    except ValueError:
        err_html = """
        <div class='result-card error-card'>
          <div class='result-icon'>⚠️</div>
          <div class='result-label'>Invalid Date</div>
          <div class='result-sub'>Please use DD-MM-YYYY format</div>
        </div>"""
        return err_html, _build_history_html()

    features  = [[log_amount, month, day]]
    pred      = rf.predict(features)[0]
    proba     = rf.predict_proba(features)[0]
    confidence = round(float(max(proba)) * 100, 1)
    label     = "Income" if pred == 1 else "Expense"

    # colours & emoji
    if label == "Income":
        color, bg, icon = "#10b981", "rgba(16,185,129,0.12)", "💰"
    else:
        color, bg, icon = "#f43f5e", "rgba(244,63,94,0.12)", "💸"

    bar_pct = confidence
    result_html = f"""
    <div class='result-card' style='border-color:{color}; background:{bg};'>
      <div class='result-icon'>{icon}</div>
      <div class='result-label' style='color:{color};'>{label}</div>
      <div class='result-sub'>Model confidence</div>
      <div class='conf-bar-wrap'>
        <div class='conf-bar-fill' style='width:{bar_pct}%; background:{color};'></div>
      </div>
      <div class='conf-pct' style='color:{color};'>{confidence}%</div>
      <div class='result-meta'>₹{amount:,.0f} &nbsp;·&nbsp; {date_str}</div>
    </div>"""

    # log history (keep latest 10)
    _history.insert(0, {
        "date": date_str, "amount": f"₹{amount:,.0f}",
        "label": label, "conf": f"{confidence}%", "color": color
    })
    del _history[10:]

    return result_html, _build_history_html()


def _build_history_html():
    if not _history:
        return "<div class='empty-history'>No predictions yet. Run a prediction above!</div>"
    rows = ""
    for h in _history:
        rows += f"""
        <tr>
          <td>{h['date']}</td>
          <td>{h['amount']}</td>
          <td><span class='badge' style='background:{h['color']}22;color:{h['color']};border:1px solid {h['color']}44'>{h['label']}</span></td>
          <td>{h['conf']}</td>
        </tr>"""
    return f"<table class='hist-table'><thead><tr><th>Date</th><th>Amount</th><th>Type</th><th>Confidence</th></tr></thead><tbody>{rows}</tbody></table>"


